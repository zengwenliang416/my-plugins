#!/bin/bash
# =============================================================================
# handover-assistant.sh - 团队交接助理 Hook
# =============================================================================
# 用途: 会话结束时生成结构化交接文档，方便团队协作
# 触发: SessionEnd
# 类型: prompt (输出指令注入上下文)
# 优先级: P1 - 团队协作
# =============================================================================

cat << 'EOF'
📋 **会话结束 - 请生成交接文档**

在结束本次会话前，请生成一份结构化的交接文档，包含：

## 1. ✅ 已完成的任务
- 列出本次会话完成的主要工作
- 简要说明每项工作的结果

## 2. ⏳ 未完成的事项
- 列出尚未完成的任务
- 说明当前进度和下一步操作

## 3. ⚠️ 已知问题和阻塞点
- 记录遇到的问题和障碍
- 提供可能的解决方向

## 4. 📝 重要上下文
- 记录关键的技术决策
- 保留重要的配置或路径信息

---
请将此交接文档输出为 Markdown 格式，便于后续会话或团队成员快速了解现状。
EOF
