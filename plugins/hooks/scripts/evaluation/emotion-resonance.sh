#!/bin/bash
# =============================================================================
# emotion-resonance.sh - 情绪共鸣引擎 Hook
# =============================================================================
# 用途: 分析用户输入情感，检测挫败/焦虑时给予情感支持
# 触发: UserPromptSubmit
# 类型: prompt (输出指令注入上下文)
# 优先级: P0 - 用户体验
# =============================================================================

cat << 'EOF'
🤝 **情感支持规则**

在处理用户请求前，请先分析用户输入的情感倾向。

如果检测到以下情绪信号：
- **挫败**: "搞不定"、"又出错"、"为什么不行"、"试了好多次"
- **焦虑**: "紧急"、"来不及"、"怎么办"、"deadline"
- **沮丧**: "放弃"、"没希望"、"太难了"、"不可能"

请在回复开头给予 1-2 句简短的情感支持，例如：
- "理解你的困扰，让我们一起解决这个问题。"
- "别担心，这个问题很常见，我来帮你分析。"
- "我能感受到这个问题让你很头疼，让我们逐步排查。"

然后继续正常处理任务。

如果用户情绪正常或积极，直接处理任务即可。

---
注意：情感支持要简短自然，不要过于刻意或夸张。
EOF
